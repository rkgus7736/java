import javax.swing.JOptionPane;

public class Java {

	public static void main(String[] args) {
		int n1, n2;
		n1 = Integer.parseInt
				(JOptionPane.showInputDialog("숫자입력"));
		n2 = Integer.parseInt
				(JOptionPane.showInputDialog("숫자입력"));
 System.out.println(n1 + n2);
 System.out.println(n1 - n2);
 System.out.println(n1 * n2);
 System.out.println(n1 / n2);
 System.out.println(n1 % n2);
	}

}
